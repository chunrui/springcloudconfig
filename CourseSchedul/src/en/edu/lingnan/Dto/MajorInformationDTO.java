package en.edu.lingnan.Dto;

public class MajorInformationDTO {
	String MajorID=null;
	String MajorName=null;
	int MIflag;
	public String getMajorID() {
		return MajorID;
	}
	public void setMajorID(String majorID) {
		MajorID = majorID;
	}
	public String getMajorName() {
		return MajorName;
	}
	public void setMajorName(String majorName) {
		MajorName = majorName;
	}
	public int getMIflag() {
		return MIflag;
	}
	public void setMIflag(int mIflag) {
		MIflag = mIflag;
	}

}
